    public void updateBillByTable(int tableNumber) {
        int ndx = tableNumber - 1;
        tableNumberLabel.setText("" + tableNumber);
        if (Counter.table[ndx] == null) {
            this.clearBill();
            this.chargeButton.setText("Charge");
            this.orderNumberLabel.setText("#0000");
        } else {
            //Food Section
            personNumberLabel.setText("" + Counter.table[ndx].getNumberOfPeople());
            startTimeLabel.setText(Counter.table[ndx].getEnteredTime());
            qtyFoodLabel.setText("<html>");
            descriptionFoodLabel.setText("<html>");
            priceFoodLabel.setText("<html>");
            orderNumberLabel.setText("#"+Counter.table[ndx].getOrder());
            for (Food i : Counter.table[ndx].foodOrdered.keySet()) {
                qtyFoodLabel.setText(qtyFoodLabel.getText() + "<br>" + Counter.table[ndx].foodOrdered.get(i)); //******************************************//
                descriptionFoodLabel.setText(descriptionFoodLabel.getText() + "<br>" + i.getName(0));
                priceFoodLabel.setText(priceFoodLabel.getText() + "<br>" + String.format("฿%.2f", i.getPrice() * Counter.table[ndx].foodOrdered.get(i)));
            }
            qtyFoodLabel.setText(qtyFoodLabel.getText() + "</html>");
            descriptionFoodLabel.setText(descriptionFoodLabel.getText() + "</html>");
            priceFoodLabel.setText(priceFoodLabel.getText() + "</html>");

            //Drinks Section
            qtyDrinksLabel.setText("<html>");
            descriptionDrinksLabel.setText("<html>");
            priceDrinksLabel.setText("<html>");

            for (Food i : Counter.table[ndx].drinksOrdered.keySet()) {
                qtyDrinksLabel.setText(qtyDrinksLabel.getText() + "<br>" + Counter.table[ndx].drinksOrdered.get(i)); //******************************************//
                descriptionDrinksLabel.setText(descriptionDrinksLabel.getText() + "<br>" + i.getName(0));
                priceDrinksLabel.setText(priceDrinksLabel.getText() + "<br>" + String.format("฿%.2f", i.getPrice() * Counter.table[ndx].drinksOrdered.get(i)));
            }
            qtyDrinksLabel.setText(qtyDrinksLabel.getText() + "</html>");
            descriptionDrinksLabel.setText(descriptionDrinksLabel.getText() + "</html>");
            priceDrinksLabel.setText(priceDrinksLabel.getText() + "</html>");

            //Money Section
            vatCostLabel.setText(String.format("฿ %.2f", Counter.table[ndx].getVAT()));
            serviceChargeCostLabel.setText(String.format("฿ %.2f", Counter.table[ndx].getServiceCharge()));
            totalCostLabel.setText(String.format("฿ %.2f", Counter.table[ndx].getTotalPrice()));
            this.chargeButton.setText("Charge  ฿ " + String.format("%.2f", Counter.table[ndx].getTotalPrice()));
        }

    }

