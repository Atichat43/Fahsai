    private void updatePasscode(int n) {
        this.passcode += n;
        System.out.println(passcode);
        updatePasscodeAnimation();
        if (this.passcode.length() == 4) {
            tryToLogin();
        }
    }

    private void updatePasscodeAnimation() {
        javax.swing.JLabel[] loginTab = {this.loginTab1, this.loginTab2, this.loginTab3, this.loginTab4};
        for (int i = 0; i < 4; ++i) {
            if (i < this.passcode.length()) {
                loginTab[i].setIcon(new javax.swing.ImageIcon(getClass().getResource("/finalproject/pic/circle_blue.png")));
            } else {
                loginTab[i].setIcon(new javax.swing.ImageIcon(getClass().getResource("/finalproject/pic/circle_gray.png")));
            }
        }
    }

    private void tryToLogin() {
        this.loginTab4.setIcon(new javax.swing.ImageIcon(getClass().getResource("/finalproject/pic/circle_blue.png")));
        if (this.passcode.equals(CORRECT_PASSCODE)) {
            this.loginPanel.setVisible(false);
            this.mainPanel.setVisible(true);
        } else {
            javax.swing.JLabel[] loginTab = {this.loginTab1, this.loginTab2, this.loginTab3, this.loginTab4};
            for (int i = 0; i < 4; ++i) {
                loginTab[i].setIcon(new javax.swing.ImageIcon(getClass().getResource("/finalproject/pic/circle_gray.png")));
            }
            this.passcode = "";
        }
    }

    private void cashierAddMoney(String s) {
        if (s.equals("C")) {
            if (this.moneyPaid.length() > 1) {
                this.moneyPaid = this.moneyPaid.substring(0, this.moneyPaid.length() - 1);
            } else if (this.moneyPaid.length() == 1) {
                this.moneyPaid = "0";
            }
        } else if (s.equals("100")) {
            if (this.moneyPaid.contains(".")) {
                double temp = Double.parseDouble(this.moneyPaid);
                temp += 100;
                this.moneyPaid = String.valueOf(temp);

            } else {
                int temp = Integer.parseInt(this.moneyPaid);
                temp += 100;
                this.moneyPaid = String.valueOf(temp);
            }
        } else if (s.equals("500")) {
            if (this.moneyPaid.contains(".")) {
                double temp = Double.parseDouble(this.moneyPaid);
                temp += 500;
                this.moneyPaid = String.valueOf(temp);

            } else {
                int temp = Integer.parseInt(this.moneyPaid);
                temp += 500;
                this.moneyPaid = String.valueOf(temp);
            }
        } else if (s.equals("1000")) {
            if (this.moneyPaid.contains(".")) {
                double temp = Double.parseDouble(this.moneyPaid);
                temp += 1000;
                this.moneyPaid = String.valueOf(temp);

            } else {
                int temp = Integer.parseInt(this.moneyPaid);
                temp += 1000;
                this.moneyPaid = String.valueOf(temp);
            }
        } else if (s.equals(".")) {
            if (!this.moneyPaid.contains(".")) {
                this.moneyPaid += s;
            }
        } else if (this.moneyPaid.length() < 8) {
            if (this.moneyPaid.equals("0")) {
                this.moneyPaid = s;
            } else {
                this.moneyPaid += s;
            }
        }
        updateCashierStatus();

    }

    private void updateCashierStatus() {
        this.cashierBalanceDueLabel.setText("฿ " + String.format("%.2f", Counter.table[this.chosenTable - 1].getTotalPrice()));
        this.cashierAmountLabel.setText("฿ " + this.moneyPaid);
    }

    private void calculateChange() {
        double change = Double.parseDouble(this.moneyPaid) - Counter.table[this.chosenTable - 1].getTotalPrice();
        if (change >= 0) {
            this.haveSuccesfullyPaid = true;
            this.cashierChangeNumLabel.setText("฿ " + String.format("%.2f", change));
        } else {
            this.cashierChangeNumLabel.setText("Invalid");
        }

    }

