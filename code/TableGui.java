    public void updateCategoryList() {
        DefaultListModel categoryListModel = (DefaultListModel) foodCategoryList.getModel();
        categoryListModel.removeAllElements();

        if (selectedLanguage == 0) {
            for (String i : this.foodCategoryEnglish) {
                categoryListModel.addElement(i);
            }
        } else {
            for (String i : this.foodCategoryThai) {
                categoryListModel.addElement(i);
            }
        }
        foodCategoryList.setModel(categoryListModel);
    }

