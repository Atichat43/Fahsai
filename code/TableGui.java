    private void removeButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_removeButtonMouseClicked
        // TODO add your handling code here:
        int foodNdx = foodList.getSelectedIndex();
        int drinksNdx = drinksList.getSelectedIndex();
        System.out.println(removingSelection);
        if (removingSelection == 0) {
            //delete food
            String removingName = foodList.getSelectedValue().split("\\.")[0];
            for (Food i : selectedFood.keySet()) {
                System.out.println(i.getName(selectedLanguage) + "\t" + removingName);

                if (i.getName(selectedLanguage).equals(removingName)) {
                    totalOrderSelection -= selectedFood.get(i);
                    selectedFood.remove(i);
                }
            }

            DefaultListModel foodListModel = (DefaultListModel) foodList.getModel();
            foodListModel.removeElementAt(foodNdx);
            foodList.setModel(foodListModel);

        } else if (removingSelection == 1) {
            //delete drinks
            String removingName = drinksList.getSelectedValue().split("\\.")[0];
            System.out.println(removingName);
            System.out.println(selectedDrinks.size());
            for (Food i : selectedDrinks.keySet()) {
                if (i.getName(selectedLanguage).equals(removingName)) {
                    totalOrderSelection -= selectedDrinks.get(i);
                    selectedDrinks.remove(i);
                }
            }
            System.out.println("afterloop");
            DefaultListModel drinksListModel = (DefaultListModel) drinksList.getModel();
            drinksListModel.removeElementAt(drinksNdx);
            drinksList.setModel(drinksListModel);

        }
        drinksList.setSelectedIndex(-1);
        foodList.setSelectedIndex(-1);


    }//GEN-LAST:event_removeButtonMouseClicked

    private void sendOrderButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_sendOrderButtonMouseClicked

        try {
            // TODO add your handling code here:
            //client.send(new Order(this.selectedFood, this.selectedDrinks, this.thisTable.id));
            client.send(new Order(new HashMap<Food, Integer>(selectedFood), new HashMap<Food, Integer>(selectedDrinks), this.thisTable.id));
        } catch (IOException ex) {
            //Logger.getLogger(TableGui.class.getName()).log(Level.SEVERE, null, ex);
            JFrame frame = new JFrame("Error Occured");
            JOptionPane.showMessageDialog(frame, "Lost Connection with Host", "Host Unknown", JOptionPane.ERROR_MESSAGE);
        }

        for (Food i : selectedFood.keySet()) {
            db.updateCount(i, selectedFood.get(i));
        }
        for (Food i : selectedDrinks.keySet()) {
            db.updateCount(i, selectedDrinks.get(i));
        }

        this.thisTable.addOrderd(this.selectedFood, this.selectedDrinks);
        this.selectedFood.clear();
        this.selectedDrinks.clear();
        this.removingSelection = -1;

        DefaultListModel foodListModel = (DefaultListModel) foodList.getModel();
        foodListModel.removeAllElements();

        DefaultListModel drinksListModel = (DefaultListModel) drinksList.getModel();
        drinksListModel.removeAllElements();

        foodList.setModel(foodListModel);
        drinksList.setModel(drinksListModel);

        System.out.println(selectedFood.size());
        totalOrder += totalOrderSelection;
        totalOrderSelection = 0;


    }//GEN-LAST:event_sendOrderButtonMouseClicked

