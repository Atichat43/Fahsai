    private void updateSelectedMenu() {
        System.out.println("select");

        DefaultListModel foodListModel = (DefaultListModel) foodList.getModel();
        foodListModel.removeAllElements();

        DefaultListModel drinksListModel = (DefaultListModel) drinksList.getModel();
        drinksListModel.removeAllElements();

        for (Food i : selectedFood.keySet()) {
            String s = String.format("%-50s %-14d ฿%8.2f", i.getName(selectedLanguage) + ".", selectedFood.get(i), i.getPrice() * selectedFood.get(i));
            foodListModel.addElement(s);
        }

        for (Food i : selectedDrinks.keySet()) {
            String s = String.format("%-50s %-14d ฿%8.2f", i.getName(selectedLanguage) + ".", selectedDrinks.get(i), i.getPrice() * selectedDrinks.get(i));
            drinksListModel.addElement(s);
        }

        foodList.setModel(foodListModel);
        drinksList.setModel(drinksListModel);
        System.out.println("selected");
    }

