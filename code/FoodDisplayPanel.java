    private void setImage() {
        this.foodIconLabel.setIcon(this.food.getImage());
    }

    private void setName() {
        String name = this.food.getName(selectedLanguage);
        if (name.length() > 20) {
            String temp = "<html><div align=\"center\">";
            int countSpace = 0;
            for (int i = 0; i < name.length(); ++i) {
                if (name.charAt(i) == ' ') {
                    ++countSpace;
                    if (countSpace == 3) {
                        temp += "<br>";
                    }
                }
                temp += name.charAt(i);
            }
            temp += "</div></html>";
            name = new String(temp);
        }
        this.foodNameLabel.setText(name);
    }

